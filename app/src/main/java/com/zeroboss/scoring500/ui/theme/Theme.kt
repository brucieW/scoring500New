@Composable
fun Scoring500Theme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    MaterialTheme(
        colors = if (darkTheme) DarkColorPalette else LightColorPalette,
        typography = typography,
        shapes = shapes,
        content = content
    )
}

object ScoringTheme {
    val colors: Scoring500Colors
        @Composable
        get() = LocalScoring500Colors.current
}

